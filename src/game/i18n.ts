export function skillDesc(id: string, lv: number, lang?: Lang): string {
  const l = lang || currentLang;
  const key = `${id}_${Math.min(lv - 1, 5)}`;
  return SKILL_DESCS[l]?.[key] || SKILL_DESCS['ko'][key] || key;
}

export function mobName(type: string, lang?: Lang): string {
  const l = lang || currentLang;
  return MOB_NAMES[l]?.[type] || MOB_NAMES['en'][type] || type;
}
